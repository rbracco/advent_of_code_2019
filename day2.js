function runCode(inp){
    ind = 0
    op = inp[ind]
    while(typeof op !== "undefined" && op !== 99){
        if (op === 1) {
            inp[inp[ind+3]] = inp[inp[ind+1]] + inp[inp[ind+2]];
        }
        else if (op === 2) {
            inp[inp[ind+3]] = inp[inp[ind+1]] * inp[inp[ind+2]];
        }
        ind += 4;
        op = inp[ind]
    }
    return inp
}

